package lectureCode;

public class Address {
	String streetNum;
	String streetName;
	String suburb;
	String city;
	int postCode;
	
	public Address(String streetNum, String streetName, String suburb, String city, int postcode) {
		this.streetNum = streetNum;
		this.streetName = streetName;
		this.suburb = suburb;
		this.city = city;
		this.postCode = postCode;
		
	}
}
